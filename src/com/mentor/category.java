package com.mentor;

public class category {

	
	String cat;
	String image;
	public category(String cat, String image) {
		super();
		this.cat = cat;
		this.image = image;
	}
	public String getCat() {
		return cat;
	}
	public void setCat(String cat) {
		this.cat = cat;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	
}
